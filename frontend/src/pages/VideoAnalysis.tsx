type PersonEntry = { id: string; name: string; avatar: string; percent: number; face_snap_base64?: string | null; face_from_video_base64?: string | null; clips: Array<{ start: number; end: number; score: number }> }

type ClipMatch = { start: number; end: number; score: number; type: string }

function fmtSec(seconds: number): string {
  if (!Number.isFinite(seconds) || seconds < 0) return '—'
  const h = Math.floor(seconds / 3600)
  const m = Math.floor((seconds % 3600) / 60)
  const s = Math.floor(seconds % 60)
  if (h > 0) return `${h}:${m.toString().padStart(2, '0')}:${s.toString().padStart(2, '0')}`
  return `${m}:${s.toString().padStart(2, '0')}`
}

/** Parse "M:SS" or "H:MM:SS" to seconds for video seek. */
function parseTimestampToSeconds(timestamp: string): number {
  const s = String(timestamp).trim()
  if (!s) return NaN
  const parts = s.split(':').map((p) => parseInt(p, 10))
  if (parts.some((n) => !Number.isFinite(n) || n < 0)) return NaN
  if (parts.length === 2) return parts[0] * 60 + parts[1]
  if (parts.length === 3) return parts[0] * 3600 + parts[1] * 60 + parts[2]
  if (parts.length === 1) return parts[0]
  return NaN
}

function clipRelevance(clips: ClipMatch[]): { label: string; color: string } {
  if (!clips.length) return { label: '', color: '' }
  const best = Math.max(...clips.map((c) => c.score))
  if (best >= 0.10) return { label: 'Highest', color: 'bg-emerald-100 text-emerald-800 border-emerald-300' }
  if (best >= 0.08) return { label: 'High', color: 'bg-green-100 text-green-800 border-green-300' }
  if (best >= 0.06) return { label: 'Medium', color: 'bg-yellow-100 text-yellow-800 border-yellow-300' }
  return { label: 'Low', color: 'bg-red-50 text-red-700 border-red-200' }
}

function clipScoreColor(score: number): string {
  if (score >= 0.10) return 'text-emerald-700'
  if (score >= 0.08) return 'text-green-700'
  if (score >= 0.06) return 'text-yellow-700'
  return 'text-red-600'
}

function clipScoreBg(score: number): string {
  if (score >= 0.10) return 'bg-emerald-50 border-emerald-100'
  if (score >= 0.08) return 'bg-green-50 border-green-100'
  if (score >= 0.06) return 'bg-yellow-50 border-yellow-100'
  return 'bg-red-50 border-red-100'
}

function clipScoreLabel(score: number): string {
  if (score >= 0.10) return 'Highest'
  if (score >= 0.08) return 'High'
  if (score >= 0.06) return 'Medium'
  return 'Low'
}

